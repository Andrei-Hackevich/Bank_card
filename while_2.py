#Пользователь вводит два числа с клавиатуры, необходимо вывести на экран все отрицательные числа, лежащие между ними.

a=int(input('Введите первое любое целое число (отрицательное или положительное):'))
b=int(input('Введите второе любое целое число (отрицательное или положительное):'))

if a<b and a<0:
    if a==-1:
        print('Отрицательных чисел между вашими числами нет')
    else:
        while a<0:
            a+=1
            if a==0:
                break
            else:
                print(a)
elif b<a and b<0:
    if b==-1:
        print('Отрицательных чисел между вашими числами нет')
    else:
        while b<0:
            b +=1
            if b==0:
                break
            print(b)
else:
    print('Вы не вводили отрицательных чисел')


